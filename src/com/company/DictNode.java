package com.company;

public class DictNode {
    private char a;
    private String code;

    public DictNode(char a, String code) {
        this.a = a;
        this.code = code;
    }

    public char getA() {
        return a;
    }

    public void setA(char a) {
        this.a = a;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
